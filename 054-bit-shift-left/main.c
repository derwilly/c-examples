/* bit shift left */

#include <stdio.h>

int main(void)
{
    printf("1 << 0 = %d\n",  1 << 0);
    printf("1 << 1 = %d\n",  1 << 1);
    printf("1 << 2 = %d\n",  1 << 2);
    printf("1 << 3 = %d\n",  1 << 3);
    printf("1 << 4 = %d\n",  1 << 4);
    printf("1 << 10 = %d\n", 1 << 10);
    printf("\n");

    return 0;
}
