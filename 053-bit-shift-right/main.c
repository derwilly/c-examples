/* bit shift right */

#include <stdio.h>

int main(void)
{
    printf("1024 >> 0 = %d\n",  1024 >> 0);
    printf("1024 >> 1 = %d\n",  1024 >> 1);
    printf("1024 >> 2 = %d\n",  1024 >> 2);
    printf("1024 >> 3 = %d\n",  1024 >> 3);
    printf("1024 >> 4 = %d\n",  1024 >> 4);
    printf("1024 >> 5 = %d\n", 1024 >> 5);
    printf("1024 >> 6 = %d\n", 1024 >> 6);
    printf("1024 >> 7 = %d\n", 1024 >> 7);
    printf("1024 >> 8 = %d\n", 1024 >> 8);
    printf("1024 >> 9 = %d\n", 1024 >> 9);
    printf("1024 >> 10 = %d\n", 1024 >> 10);
    printf("\n");

    return 0;
}
